import Navbar from "./navBar/navbar";
import Footer from "./footer/footer";
import Head from "next/head";
import useSWR from "swr";
import { getSession } from "next-auth/react";

//User Data fetching
const fetcher = async (url, context) => {
  const session = await getSession(context);
  const datas = await fetch(url, {
    method: "GET",
    headers: {
      Authorization: `Bearer ${session?.user.token}`,
    },
  }).then((res) => res.json());

  return datas.data;
};

export default function Layout({ children, icon, title }) {
  const { data: userData } = useSWR(`http://127.0.0.1:8000/api/user`, fetcher);
  return (
    <>
      <Head>
        <meta
          name="Find University in Cambodia"
          content="Generated by create next app"
        />
        <meta name="viewport" content="initial-scale=1.0, width=device-width" />
        <link rel="icon" type="image/png" sizes="16x16" href={icon} />
        <title>{title}</title>
      </Head>
      <Navbar session={userData} />
      <main className="min-h-screen content">{children}</main>
      <Footer />
    </>
  );
}
